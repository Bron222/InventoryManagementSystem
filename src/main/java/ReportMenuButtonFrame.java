    private void loadLowStockReport() {
        
    String[] columnNames = {"Product ID", "Product Name", "Stock Quantity", "Low Stock Alert"};
    
    DefaultTableModel tableModel = new DefaultTableModel(columnNames, 0) {
        @Override
        public boolean isCellEditable(int row, int column) {
            return false;
        }
    };
    
    lowStockTable.setModel(tableModel); // apply model to table
    tableModel.setRowCount(0); // clear any existing rows

    String query = """
        SELECT product_id, name, stock_quantity, low_stock_threshold
        FROM products
        WHERE stock_quantity <= low_stock_threshold
        """;

    try (Connection conn = DatabaseUtil.getConnection();
         PreparedStatement stmt = conn.prepareStatement(query);
         ResultSet rs = stmt.executeQuery()) {

        while (rs.next()) {
            int id = rs.getInt("product_id");
            String name = rs.getString("name");
            int qty = rs.getInt("stock_quantity");
            int threshold = rs.getInt("low_stock_threshold");

            tableModel.addRow(new Object[]{id, name, qty, threshold});
        }
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error loading low stock report: " + e.getMessage());
    }
}

