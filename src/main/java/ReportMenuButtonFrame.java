    private void loadReportData() {
        
        try (Connection conn = DatabaseUtil.getConnection()) {

        // Query DB for total sales
        String salesQuery = "SELECT SUM(total_amount) AS total_sales FROM sales";
        PreparedStatement salesStmt = conn.prepareStatement(salesQuery);
        ResultSet salesRs = salesStmt.executeQuery();
        double totalSales = 0.0;
        if (salesRs.next()) {
            totalSales = salesRs.getDouble("total_sales");
        }

        // Query DB for total purchases
        String purchasesQuery = "SELECT SUM(total_amount) AS total_purchases FROM purchaseorders";
        PreparedStatement purchasesStmt = conn.prepareStatement(purchasesQuery);
        ResultSet purchasesRs = purchasesStmt.executeQuery();
        
        double totalPurchases = 0.0;
        
        if (purchasesRs.next()) {
            totalPurchases = purchasesRs.getDouble("total_purchases");
        }
        
        NumberFormat currencyFormat = NumberFormat.getCurrencyInstance();

        // Set values in the labels
        totalSalesLabel.setText(currencyFormat.format(totalSales));
        totalPurchasesLabel.setText(currencyFormat.format(totalPurchases));
        grossProfitLabel.setText(currencyFormat.format(totalSales - totalPurchases));

        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading report data: " + e.getMessage());
        }
    }
